#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	dig [12];
	int		i;

	i = 0;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	while (nb / 10 != 0)
	{
		dig[i++] = nb % 10 + '0';
		nb /= 10;
	}
	dig[i++] = nb + '0';
	dig[i--] = '\0';
	while (i >= 0)
	{
		write(1, &dig[i], 1);
		i--;
	}
}
